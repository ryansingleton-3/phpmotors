// Get a list of vehicles based on classification
function getVehiclesByClassification($classificationName) {
    $db = phpmotorsConnect();
    $sql = 'SELECT inventory.*, images.imgPath AS thumbnailPath
            FROM inventory
            LEFT JOIN (
                SELECT img.*
                FROM images AS img
                WHERE img.imgPath LIKE "%-tn%"
                AND img.imgId IN (
                    SELECT MIN(imgId)
                    FROM images
                    GROUP BY invId
                )
            ) AS images ON inventory.invId = images.invId
            WHERE inventory.classificationId IN (
                SELECT classificationId
                FROM carclassification
                WHERE classificationName = :classificationName
            )';
    $stmt = $db->prepare($sql);
    $stmt->bindValue(':classificationName', $classificationName, PDO::PARAM_STR);
    $stmt->execute();
    $vehicles = $stmt->fetchAll(PDO::FETCH_ASSOC);
    $stmt->closeCursor();
    return $vehicles;
}





